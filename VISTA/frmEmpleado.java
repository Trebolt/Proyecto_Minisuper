     public void buscarEmpleado()
     {
         Empleado emp = null;
        try {
            emp = datosEmpleado.buscarEmpleadoDNI(Integer.parseInt(jtfDniE.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (emp != null) {
            try {
                jtfNroEmpleado.setText(String.valueOf(emp.getNroEmpleado()));
                jtfDniE.setText(String.valueOf(emp.getDni()));
                jtfNombreE.setText(emp.getNombre());
                jtfApellidoE.setText(emp.getApellido());
                jtfEdadE.setText(String.valueOf(emp.getEdad()));
                jtfTelefonoE.setText(emp.getTelefono());
                jcboSexoE.setSelectedItem(emp.getSexo());
                jtfCelularE.setText(emp.getCelular());
                jtfEmailE.setText(emp.getEmail());
                jtfDireccionE.setText(emp.getDireccion());
                jtfUsuario.setText(emp.getUser());
                URL url = new URL(emp.getImagen());
                m.setObtener(url);
                m.Mostrar(jpnImagen);
            } catch (MalformedURLException ex) {
                Logger.getLogger(frmEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No exite");
        }
     }
