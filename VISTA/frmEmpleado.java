    private void jbnAgregarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnAgregarEActionPerformed
        // TODO add your handling code here:
        frmUsuario usr = new frmUsuario();
   if(jtfDniE.getText().compareTo("")!=0 && jtfNombreE.getText().compareTo("")!=0&& jtfApellidoE.getText().compareTo("")!=0 
      && jtfEdadE.getText().compareTo("")!=0 && (jtfCelularE.getText().compareTo("")!=0 || jtfTelefonoE.getText().compareTo("")!=0)
      && jtfDireccionE.getText().compareTo("")!=0){
        Empleado em = new Empleado() {};
        em.setDni(Integer.parseInt(jtfDniE.getText()));
        em.setNombre(jtfNombreE.getText());
        em.setApellido(jtfApellidoE.getText());
        em.setEdad(Integer.parseInt(jtfEdadE.getText()));
        em.setTelefono((jtfTelefonoE.getText()));
        em.setSexo((String) jcboSexoE.getSelectedItem());
        em.setCelular(jtfCelularE.getText());
        em.setEmail(jtfEmailE.getText());
        em.setDireccion(jtfDireccionE.getText());
        em.setImagen(ImagenURL);
        try {
            datosEmpleado.insertarEmpleado(em, usr);
           
            LimpiarTextos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
        }
        JOptionPane.showMessageDialog(null, "[ Datos Agregados ]","Correcto",JOptionPane.INFORMATION_MESSAGE);
   }else{
       JOptionPane.showMessageDialog(null, "Llene los Campos Obligatorios","Error: Datos no Validos",JOptionPane.ERROR_MESSAGE);
       getToolkit().beep();        //Emite un sonido de alerta
   }

    }//GEN-LAST:event_jbnAgregarEActionPerformed

    private void jbnBuscarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnBuscarEActionPerformed
        // TODO add your handling code here:
        if (jtfDniE.getText().compareTo("")!= 0) {
            int result = JOptionPane.showConfirmDialog(
                    this, "Deseas Buscar al Empleado para Modificarlo?",
                    "Mensaje..!!",
                    JOptionPane.YES_NO_OPTION);

            if (result == JOptionPane.YES_OPTION) {
                buscarEmpleado();
                jbnNuevoE.setEnabled(false);
                jbnMostrarE.setEnabled(false);
                jbnCancelarE.setEnabled(true);
                jbnAbrirE.setEnabled(true);
                jbnEditarE.setEnabled(true);
                activarTextos(true);
            }
            if (result == JOptionPane.NO_OPTION) {
                buscarEmpleado();
                jbnEliminarE.setEnabled(true);
                jbnNuevoE.setEnabled(false);
                jbnCancelarE.setEnabled(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese DNI que Desea Buscar");
        }
    }//GEN-LAST:event_jbnBuscarEActionPerformed

    private void jbnEliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnEliminarEActionPerformed
        // TODO add your handling code here:
        try {
            Empleado emp = datosEmpleado.buscarEmpleadoDNI(Integer.parseInt(jtfDniE.getText()));
            if (emp == null) {
                JOptionPane.showMessageDialog(null, "No existe");
            } else {
                datosEmpleado.eliminarEmpleadoDNI(emp.getDni());
                JOptionPane.showMessageDialog(null, "Se Elimino Exitosamente..OKz!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
        }
        LimpiarTextos();
    }//GEN-LAST:event_jbnEliminarEActionPerformed

